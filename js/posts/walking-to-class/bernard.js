// EE = 1.44 + 1.94*S^0.43 + 0.24*S^4 + 0.34*S*G*(1-1.05^(1-1.11^(G+32)))

//speed = [[.9, 'slow'], [1.35, 'normal'], [1.8, 'fast']]

function EE(speed, incline) {
speed = parseFloat(speed);
incline = parseFloat(incline);
var a = 1.94*Math.pow(speed, .43);
var b = .24*Math.pow(speed, 4);
var c = 1 - Math.pow(1.11, incline+32);
var d = 0.34*speed*incline*(1-Math.pow(1.05, c));
var ee = 1.44 + a + b + d;
return ee;
}

