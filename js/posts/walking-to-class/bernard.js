function gendata(num, opt, type) {
  var dis = [];
  if (opt === false) {
    for (i = 1; i < 5; i++) dis.push(data[num + 2 * i][type].toFixed(2));
  } else if (opt === true) {
    for (i = 1; i < 5; i++) dis.push(data[num + 2 * i + 1][type].toFixed(2));
  } else {
    alert('error');
    return;
  }
  return dis;
}

